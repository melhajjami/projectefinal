    {
        //DONADA ID DE USUARI, RETORNAR ELS JOCS QUE TÃ‰
        $biblioteca = biblioteca::where('id_usuari', $id)->get();
        
        $arrayjocs = [];

        foreach($biblioteca as $jocs){
            $joc = joc::where('id', $jocs->id_joc)->get();
            array_push($arrayjocs,$joc);
        }

        return $arrayjocs;  
    }

